         <p> O termo Pentecostal e derivado de Pentecostes, um termo grego que descreve a festa judaica das semanas. Para os cristaos, este evento comemora a descida do Espirito Santo sobre os seguidores de Jesus Cristo, conforme descrito no Atos 2.Pentecostais tendem a ver que seu movimento reflete o mesmo tipo de poder espiritual, estilo de adoracao e ensinamentos que foram encontrados na Igreja primitiva. Por este motivo, alguns pentecostais tambem usam o termo Apostolica ou Evangelho Pleno para descrever seu movimento.</p><br>

         <p>O pentecostalismo é um termo amplo que inclui uma vasta gama de diferentes perspectivas teologicas e organizacionais. Como resultado, nao existe nenhuma organização central ou igreja que dirige o movimento. Os pentecostais podem ser inseridos em mais de um grupo cristão, indo do trinitariano até o não-trinitariano.Muitos grupos pentecostais são afiliados ao Conferência Mundial Pentecostal. No Brasil é comum os pentecostais e os protestantes históricos (presbiterianos e luteranos, por exemplo) se autoidentificarem com o termo evangélico.</p><br>

         <p>A ênfase do pentecostalismo sobre o charisma o coloca dentro do cristianismo carismatico, um enorme agrupamento de cristão que tem aceitado alguns ensinamentos pentecostais sobre o batismo no Espírito e dons espirituais. O pentecostalismo está teologicamente e historicamente próximo ao Movimento Carismático, influenciando tão significativamente o movimento, que às vezes os termos pentecostal e carismático são usados indistintamente. Todo o movimento pentecostal no mundo inclui cerca de 590 milhões de pessoas.</p>
