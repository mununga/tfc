                    <h1>Crianças</h1>
                    <p class="departamento">
                    <p>Nossas crianças cantam e apresentam poesias, todas elas são cheias de vontades para adorar e louvar ao Senhor.Graças a Deus elas aprendem e entendem todos ensinamentos com  muita facilidade, pois delas são os reinos dos céus.</p>

                    <p>Ao começarmos a procurar uma perspectiva bíblica, chegamos à passagem definitiva do Antigo Testamento sobre a educação de crianças, encontrada em Deuteronômio 6:5-8 : "Amarás, pois, o SENHOR, teu Deus, de todo o teu coração, de toda a tua alma e de toda a tua força. Estas palavras que, hoje, te ordeno estarão no teu coração; tu as inculcarás a teus filhos, e delas falarás assentado em tua casa, e andando pelo caminho, e ao deitar-te, e ao levantar-te. Também as atarás como sinal na tua mão, e te serão por frontal entre os olhos.

                       <br>
                      <br>" A história dos hebreus revela que o pai devia ser diligente em instruir os seus filhos nos caminhos e palavras do Senhor para o seu próprio desenvolvimento espiritual e bem-estar. A mensagem nesta passagem é repetida no Novo Testamento, onde Paulo exorta os pais a educarem os filhos na "disciplina e admoestação do Senhor" (Efésios 6:4). Provérbios 22:6 diz-nos também: "Ensina a criança no caminho em que deve andar, e, ainda quando for velho, não se desviará dele." O treinamento inclui não apenas a educação formal, mas também as primeiras instruções que os pais dão a uma criança, ou seja, a sua primeira educação. Esta formação destina-se a plantar a criança firmemente na fundação sobre a qual a sua vida se baseia.<br>

                      <br>

 À medida que avançamos para o tema da educação formal, no entanto, há mal-entendidos que precisam ser abordados. Em primeiro lugar, Deus não está dizendo que só os pais devem educar os filhos, como muitos afirmam, e, em segundo lugar, Ele não está dizendo que a educação pública é ruim e que devemos educar os nossos filhos apenas em escolas cristãs ou em casa. O princípio encontrado em toda a Escritura é o da responsabilidade final. Deus nunca dirige os pais a evitar a educação fora de casa. Então, dizer que o único método "bíblico" da educação formal é em casa ou em escolas cristãs seria adicionar à Palavra de Deus, e queremos evitar o uso da Bíblia para validar as nossas próprias opiniões.
<br>
 <br> Exatamente o oposto é verdadeiro: queremos basear as nossas opiniões na Bíblia. Também queremos evitar o argumento de que apenas os professores "treinados" são capazes de educar as nossas crianças. Mais uma vez, a questão é a de responsabilidade final, que pertence aos pais.
</p>
