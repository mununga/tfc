                    <h1>Adolescentes</h1>
                    <p class="departamento">
                    <p>Existe um grupo de adolescentes na nossa igreja, denominado "<strong>Geração de Samuel"</strong> estes adolescentes abençoados cantam, tocam, apresentam jogral, peças teatrais, enfim adoram ao Senhor</p>
                    <p>O desafio de ser adolescente neste tempo passa, inclusive, pelo desafio de vencer os joios que se encontram no meio do trigo. Os joios são aqueles que ensinam a intriga ao invés de ensinarem os mandamentos de Cristo, são aqueles que lançam no rosto o passado que o próprio Deus esqueceu quando nos arrependemos recebendo a salvação através da morte de Cristo.
                      <br>
                      <br> Em resumo, o desafio de ser um adolescente no mundo atual passa pelos pais que precisam ser sábios e ensinar o caminho do Senhor (Pv 22:6; Dt 6:4-9). Passa também pela necessidade de serem ouvidos e compreendidos por nós, em serem prudentes como a serpente e mansos como a pomba quando enviados como ovelhas ao meio de lobos (Mt 10:16).</p>
                    <p> A concorrência com o mundo lá fora é grande e, na maioria das vezes, desleal, pois tudo é colocado de forma a atraí-los. Perseverar até o fim deve ser o propósito de cada um. O adolescente precisa ser estável espiritualmente falando, precisa fazer parte de uma geração radical que não se corrompe com o mundo. Pois assim está escrito: para que vos torneis irrepreensíveis e sinceros, filhos de Deus imaculados no meio de uma geração corrupta e perversa, entre a qual resplandeceis como luminares no mundo (Fp 2.15).</p>
                     <br>

                     <p>
Encontrar um amigo é o grande dilema da adolescência! Por viver intensamente, o adolescente encontra todos os dias muitas pessoas: colegas, conhecidos, primos, irmãos em Cristo, vizinhos... mas achar um amigo de verdade, não é tão fácil. É possível que você esteja amargando uma situação difícil por ter confiado em alguém que o decepcionou, e isso fez você concluir que não existem amigos, não se pode confiar em ninguém.
Por esse motivo, ao escolher as suas amizades, o adolescente cristão deve estar atento a alguns fatores que poderão influenciar sua vida para sempre, e deve ter cuidado com as amizades mundanas, pois a Bíblia afirma que as más conversações corrompem os bons costumes (I Coríntios 15:33), por isso, permanecer em companhia de pessoas erradas pode influenciar negativamente uma vida promissora.
<br>

<br> Quantos já deixaram de lado o bom costume de orar, de ler a Bíblia, de freqüentar as reuniões, de cantar louvores, e de evitar o pecado, por causa da influência de amigos que não têm o temor de Deus?
Não que haja problema em se ter amigos descrentes, mas é melhor que os melhores amigos de um cristão sejam cristãos, pois há um tipo de ajuda que somente esses podem lhe dar, a ajuda espiritual. Vale lembrar ainda que a amizade possa ser a chave da evangelização, por isso se você tem um amigo descrente, leve-o a Cristo, antes que ele o puxe para o mundo, o que é bem mais fácil.
    </p>


