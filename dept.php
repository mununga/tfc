                    <h1>Senhores</h1>
                    <p class="departamento">


                    <p> Os Senhores da nossa congregação pregam e expandem cada vez mais o envagelismo cristão, visto que é atraves da palavra que vidas são convertidas do mundo e são saradas de toda iniquidade Muitos ficam hoje surpresos que os apóstolos não tivessem denunciado a escravidão numa linguagem clara e forte com o pedido da imediata supressão do fenômeno. Os apóstolos, porém, não se consideravam reformadores sociais.
                      <br>
                      <br> Eram, antes de tudo, arautos das boas-novas da salvação em Cristo. Todavia, não fecharam os olhos à escravidão. Na verdade, anunciavam os verdadeiros princípios (como o da absoluta igualdade espiritual entre senhores e escravos), que acabou destruindo essa terrível mancha da civilização. </p><br>


                    <p>Paulo lembra aos senhores que também eles têm obrigações. Ao proceder assim, estava dando expressão a uma ideia verdadeiramente radical, porquanto naqueles tempos era cos­tume pensar que os escravos não tinham direitos. O versículo 9 contém três coisas: um princípio, uma proibição e um estímulo à consideração das palavras do apóstolo.

<br>O princípio é geralmente estabelecido desta forma: "Vós, senhores, de igual modo procedei para com eles". Deveis agir para com vossos escravos com o mesmo respeito à vontade de Deus e à autoridade de Cristo que foram concedidas a eles.



A proibição refere-se às ameaças. Muito apropriada proibi­ção, porque a ideia comum era que os escravos tinham de ser mantidos permanentemente sob controle através do medo do castigo. Os senhores cristãos tinham de abandonar totalmente essa má prática. Os que trabalhavam sob a sua autoridade deviam ser tratados com respeito e bondade, jamais com violência.
<br>
<br> Não haverá nestas palavras de Paulo algum recado para muitos dos empregadores do nosso século?



Certamente, nada menos do que a consideração.



O incentivo para agir desta maneira pode ser compreendido assim: “Sabemos que o Senhor, tanto deles como vosso, está nos céus” (v.9b). Os senhores cristãos são responsáveis diante de Deus pelo modo como tratam os seus escravos. Tanto eles como seus escravos ajoelharam-se diante do mesmo Senhor que não faz “acepção de pessoas”. Isto é, não demostra parcialidade ou favoritismo.</p>

